// JavaScript - Example 2 (Use Arrays)

// Bad
const fruit = ['Apple', 'Banana', 'Strawberry'];

// Regular
const FruitList = ['Apple', 'Banana', 'Strawberry'];

// Good
const fruits = ['Apple', 'Banana', 'Strawberry'];

// Better
const FruitNames = ['Apple', 'Banana', 'Strawberry'];

console.log("fruit: " + fruit);
console.log("FruitList: " + FruitList);
console.log("fruits: " + fruits);
console.log("FruitNames: " + FruitNames);