// JavaScript - Example 5 (Use Boolean)

// bad
const open = true;
const write = true;
const fruit = true;

// good
const isOpen = true;
const canWrite = true;
const hasFruit = true;

console.log("open " + typeof(open));
console.log("write " + typeof(write));
console.log("fruit " + typeof(fruit));
console.log("isOpen " + typeof(isOpen));
console.log("canWrite " + typeof(canWrite));
console.log("hasFruit " + typeof(hasFruit));